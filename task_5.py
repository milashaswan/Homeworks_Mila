x = float(input("Введите выручку фирмы "))
y = float(input("Введите издержки фирмы "))
r = x / y
if x > y:
    print(f"Фирма работает в плюс, Рентабельность: {r}")
    employee = int(input("Введите кол-во сотрудников:"))
    print(f"прибыль на 1 сотрудника {x / employee}")
elif x == y:
    print("Фирма работает в ноль")
else:
    print("Фирма работает в ноль")